void TFEditor::init_plots() {

    static const QColor channelcolors[4] = {Qt::red, Qt::green, Qt::blue, Qt::gray};
    static const QString channelnames[4] = {"Red", "Green", "Blue", "Alpha"};

    // ----------------------------------------------
    setWindowTitle(this->name.c_str());
    setLocale(QLocale(QLocale::English, QLocale::UnitedKingdom)); // period as decimal separator and comma as thousand separator

    // ----------------------------------------------
    this->setInteractions(QCP::iSelectLegend | QCP::iMultiSelect);
    this->axisRect()->setupFullAxesBox();//true);

    colorScale = new QCPColorScale(this);
    colorScale->setType(QCPAxis::atBottom);

    this->plotLayout()->addElement(1, 0, this->plotLayout()->element(0,0));
    this->plotLayout()->addElement(0, 0, colorScale);
    this->plotLayout()->setRowStretchFactor(0, 0.001);
    //colorScale->setMargins(QMargins(0, 0, 0, -10));
    //this->axisRect()->setMargins(QMargins(0, -10, 0, 0));

    // ----------------------------------------------
    legend->setVisible(true);
    legend->setSelectableParts(QCPLegend::spItems);
    legend->setSelectedBorderPen(QPen(Qt::black));
    legend->setSelectedTextColor(Qt::black);
    const QFont &lfont = legend->font();
    legend->setSelectedFont(QFont(lfont.family(), lfont.pointSize(), 2*lfont.weight(), true));

    // ----------------------------------------------
    QCPLayoutGrid *subLayout = new QCPLayoutGrid();

    subLayout->setMargins(QMargins(0, 0, 0, 10));
    subLayout->addElement(0, 0, this->legend);

    this->plotLayout()->addElement(2, 0, subLayout);
    this->plotLayout()->setRowStretchFactor(2, 0.001);

    this->legend->setFillOrder(QCPLegend::foColumnsFirst);
    // ----------------------------------------------


    QCPMarginGroup *mg_lft = new QCPMarginGroup(this);
    QCPMarginGroup *mg_rgt = new QCPMarginGroup(this);

    this->axisRect()->setMarginGroup(QCP::msLeft, mg_lft);
    subLayout->setMarginGroup(QCP::msLeft, mg_lft);
    colorScale->setMarginGroup(QCP::msLeft, mg_lft);

    this->axisRect()->setMarginGroup(QCP::msRight, mg_rgt);
    subLayout->setMarginGroup(QCP::msRight, mg_rgt);
    colorScale->setMarginGroup(QCP::msRight, mg_rgt);

    // ----------------------------------------------
    // usage of the 4 axes
    valueAxis = this->xAxis;
    tfIdxAxis = this->xAxis2;

    freqAxis = this->yAxis2;
    colorAxis = this->yAxis;

    freqAxis->setLabel("Frequency");
    valueAxis->setLabel("Function Value");
    colorAxis->setLabel("Color");
    //tfIdxAxis->setLabel("Transfer Function Index");

    // ----------------------------------------------
    // create histotgram + 4 channels graphs

    histo = this->addGraph(valueAxis, freqAxis);
    histo->setPen(QPen(QColor(191, 92, 1), 2));
    histo->setBrush(QColor(191, 92, 1, 50));
    histo->setName("Histogram");
    histo->setLineStyle(QCPGraph::lsStepCenter);
    legend->removeAt( legend->itemCount()-1 );      //dont show in legend

    for(int i = 0; i < 4; i++) {
        channels[i] = this->addGraph(tfIdxAxis, colorAxis);
        channels[i]->setPen(QPen(QBrush( channelcolors[i] ), 2.0));
        channels[i]->setName( channelnames[i] );

        activeChannels[i] = false;
    }

    freqAxis->setTicks(true);
    freqAxis->setSubTicks(false);
    freqAxis->setTickLabels(true);

    valueAxis->setTicks(true);
    valueAxis->setTickLabels(true);

    tfIdxAxis->setTicks(false);
    tfIdxAxis->setTickLabels(false);
    colorScale->axis()->setTicks(false);
}

