// based on the selection in legend, decide which channels are active
void TFEditor::get_activeChannels() {

    QList<QCPAbstractLegendItem*> items = this->legend->selectedItems();

    for(int i = 0; i < 4; i++) {

        QCPPlottableLegendItem *legenditem = legend->itemWithPlottable( this->channels[i] );

        if(legenditem == 0) {               activeChannels[i] = false;  continue;   }
        if (!items.contains(legenditem)) {  activeChannels[i] = false;  continue;   }
        activeChannels[i] = true;
    }
}

void toggle_scale(QCPAxis *axis) {

    if( axis->scaleType() == QCPAxis::stLinear) {

        axis->setScaleType(QCPAxis::stLogarithmic);

        QSharedPointer<QCPAxisTickerLog> logTicker(new QCPAxisTickerLog);
        axis->setTicker(logTicker);
        axis->setNumberFormat("eb"); // e = exponential, b = beautiful decimal powers
        axis->setNumberPrecision(0); // makes sure "1*10^4" is displayed only as "10^4"
    }
    else if( axis->scaleType() == QCPAxis::stLogarithmic) {
        axis->setScaleType(QCPAxis::stLinear);

        QSharedPointer<QCPAxisTicker> linearTicker(new QCPAxisTicker);
        axis->setTicker(linearTicker);
        axis->setNumberFormat("gb");
        axis->setNumberPrecision(6);
    }
}

