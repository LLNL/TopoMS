void TFEditor::channels2tfunc() {

    QSharedPointer<QCPGraphDataContainer> red = channels[0]->data();
    QSharedPointer<QCPGraphDataContainer> grn = channels[1]->data();
    QSharedPointer<QCPGraphDataContainer> blu = channels[2]->data();
    QSharedPointer<QCPGraphDataContainer> alp = channels[3]->data();

    for(int i = 0; i < tfsize; i++) {

        tfunc[4*i  ] = (float)red->at(i)->mainValue();
        tfunc[4*i+1] = (float)grn->at(i)->mainValue();
        tfunc[4*i+2] = (float)blu->at(i)->mainValue();
        tfunc[4*i+3] = (float)alp->at(i)->mainValue();

        //printf("%d (%f %f %f %f)\n", i, tfunc[4*i+0], tfunc[4*i+1], tfunc[4*i+2], tfunc[4*i+3]);
    }
}

void TFEditor::tfunc2channels() {

    QVector<double> key(tfsize);
    QVector<double> red(tfsize), grn(tfsize), blu(tfsize), alp(tfsize);

    for(int i = 0; i < tfsize; i++) {
        key[i] = (double)i;
        red[i] = tfunc[4*i];
        grn[i] = tfunc[4*i+1];
        blu[i] = tfunc[4*i+2];
        alp[i] = tfunc[4*i+3];

        //printf("%d (%f %f %f %f)\n", i, tfunc[4*i+0], tfunc[4*i+1], tfunc[4*i+2], tfunc[4*i+3]);
    }
    channels[0]->setData(key, red);
    channels[1]->setData(key, grn);
    channels[2]->setData(key, blu);
    channels[3]->setData(key, alp);
    replot();
}

