// histogram
double TFEditor::create_histogram(const float *f, uint fsize, uint num_bins,
                               QVector<double> &histogram, QVector<double> &bins) {

    float minval = *std::min_element(f, f+fsize);
    float maxval = *std::max_element(f, f+fsize);

    double bin_width = double (maxval - minval) / double (num_bins);
    double one_over_binwidth = 1.0 / bin_width;

    //printf(" histogram (%f %f) %f %f\n", minval, maxval, bin_width, one_over_binwidth);

    bins.resize(num_bins);
    histogram.resize(num_bins);

    for(int i = 0; i < num_bins; i++) {
        bins[i] = (double)i*bin_width + minval;
    }

    for(int i = 0; i < fsize; i++){

        int bin = floor(one_over_binwidth * f[i]);

        if(bin >= num_bins)     bin = num_bins-1;
        else if(bin < 0)        bin = 0;

        histogram[bin] ++;
    }
    return bin_width;
}

double TFEditor::create_loghistogram(const float *f, uint fsize, uint num_bins,
                               QVector<double> &histogram, QVector<double> &bins) {

    float minval = *std::min_element(f, f+fsize);
    float maxval = *std::max_element(f, f+fsize);

    minval = std::log10(1 + minval);
    maxval = std::log10(1 + maxval);

    double bin_width = double (maxval - minval) / double (num_bins);
    double one_over_binwidth = 1.0 / bin_width;

    //printf(" log histogram (%f %f) %f %f\n", minval, maxval, bin_width, one_over_binwidth);

    bins.resize(num_bins);
    histogram.resize(num_bins);

    for(int i = 0; i < num_bins; i++) {
        bins[i] = (double)i*bin_width + minval;
    }
    for(int i = 0; i < fsize; i++){

        int bin = floor(one_over_binwidth * std::log10(1 + f[i]));

        if(bin >= num_bins)     bin = num_bins-1;
        else if(bin < 0)        bin = 0;

        histogram[bin] ++;
    }
    return bin_width;
}

void TFEditor::set_histogram(uint num_bins, bool use_log) {

    if(!histo)
        return;

    if (fsize == 0)
        return;

    // create histogram
    QVector<double> histogram, bins;
    double bin_width = 0;

    if(use_log) {   bin_width = create_loghistogram(func, fsize, num_bins, histogram, bins); }
    else {          bin_width = create_histogram(func, fsize, num_bins, histogram, bins);    }

    histo->setData(bins, histogram);
    histo->setVisible(true);

    if(use_log) {   this->valueAxis->setLabel("Log(Function value)");   }
    else {          this->valueAxis->setLabel("Function value");   }

    this->freqAxis->rescale();
    this->valueAxis->setRange( bins[0], bins[bins.size()-1] + bin_width );
    this->replot();
}

