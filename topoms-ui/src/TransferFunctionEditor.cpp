float* TFEditor::read_transfer_function(const std::string &filename, size_t &tf_size){

    FILE *f = fopen(filename.c_str(), "r");
    if (!f) {
        printf(" Transfer function file %s not found!\n", filename.c_str());
        return 0;
    }

    printf(" Reading transfer function of from file %s...", filename.c_str());
    fflush(stdout);

    fscanf(f, "%d\n", &tf_size);
    float *vals = new float[tf_size*4];

    unsigned int ar; unsigned int ag; unsigned int ab; unsigned int aa;

    for (int i = 0; i < tf_size; i++) {

        fscanf(f, "%d, %d, %d, %d\n", &ar, &ag, &ab, &aa);
        vals[i*4]     = (float) ar / 255.0;
        vals[i*4 + 1] = (float) ag / 255.0;
        vals[i*4 + 2] = (float) ab / 255.0;
        vals[i*4 + 3] = (float) aa / 255.0;
    }
    fclose(f);

    printf(" Done! Read %d values!\n", tf_size);
    return vals;
}

void TFEditor::write_transfer_function(const float *vals, const size_t &tf_size, const std::string &filename) {

    FILE *f = fopen(filename.c_str(), "w");
    if (!f) {
        printf(" Could not create transfer function file!\n", filename.c_str());
        return;
    }

    printf(" Writing transfer function of from file %s...", filename.c_str());
    fflush(stdout);

    fprintf(f, "%d\n", tf_size);
    //float *tfunc = new float[tf_size*4];
    //unsigned int ar; unsigned int ag; unsigned int ab; unsigned int aa;

    for (int i = 0; i < tf_size; i++) {

        int r = (vals[4*i  ]*255.0);
        int g = (vals[4*i+1]*255.0);
        int b = (vals[4*i+2]*255.0);
        int a = (vals[4*i+3]*255.0);
        fprintf(f, "%d, %d, %d, %d\n", r, g, b, a);
    }
    fclose(f);

    printf(" Done! Wrote %d values!\n", tf_size);
}

