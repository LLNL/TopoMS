// key and mouse events
void TFEditor::keyPressEvent(QKeyEvent *event){

    if(event->modifiers() == Qt::ShiftModifier){
        this->setInteractions(QCP::iSelectLegend | QCP::iMultiSelect);
        live_mousemode = TFEditor::MMChannel;
        return;
    }


    switch(event->key()){

    // switch axis scaling on frequency axis
    case 'x':
    case 'X':
                toggle_scale(this->freqAxis);
                this->freqAxis->setRangeLower(1.0);
                replot();
                break;

    case 's':
    case 'S': {
                QString fileName = QFileDialog::getSaveFileName(this, tr("Save Transfer function"), ".",
                                                                      tr("Transfer function (*.tf)"));

                if (fileName.isEmpty() || fileName.isNull())
                    return;

                write_transfer_function(tfunc, tfsize, fileName.toStdString());
              }
              break;

    case 'l':
    case 'L': {
                QString fileName = QFileDialog::getOpenFileName(this, tr("Load Transfer function"), ".",
                                                                      tr("Transfer function (*.tf)"));

                if (fileName.isEmpty() || fileName.isNull())
                    return;

                delete tfunc;   tfunc = 0;
                init_tfunc(fileName.toStdString());
                emit tfunc_updated();
              }
              break;

    }


}

void TFEditor::keyReleaseEvent(QKeyEvent *event){

    this->setInteraction(QCP::iSelectLegend, 0);
    live_mousemode = TFEditor::MMNone;
}

void TFEditor::mousePressEvent(QMouseEvent *event) {

    //printf("mousePressEvent(%d)\n", live_mousemode);
    if (live_mousemode == TFEditor::MMChannel) {
        QCustomPlot::mousePressEvent(event);
        return;
    }

    live_mousemode = TFEditor::MMDrag;
    get_activeChannels();

    prev_c = pixelToCoord( event->pos() );
}

