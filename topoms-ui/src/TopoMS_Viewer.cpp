void TopoMSViewer::init(){

    QGLViewer::init();

#ifdef USE_GLEW
    glewInit();
	GLenum err = glewInit();
	if (GLEW_OK != err)
	{
		/* Problem: glewInit failed, something is seriously wrong. */
		fprintf(stderr, "Error: %s\n", glewGetErrorString(err));
	}
	fprintf(stdout, "Status: Using GLEW %s\n", glewGetString(GLEW_VERSION));
#endif

    // ---------------------------------------------------------
    // create a precompiled list to draw spheres
    sphereList = glGenLists(1);
    glNewList(sphereList, GL_COMPILE);
        TopoMSViewer::draw_sphere(1.0, 40, 40);
    glEndList();

    // ---------------------------------------------------------
    // initalize the viewer
    this->setFPSIsDisplayed(true);
    this->setTextIsEnabled(true);
    this->setBackgroundColor(Qt::white);
    this->setForegroundColor(Qt::black);
    this->setAnimationPeriod(1);        // milliseconds!

    glPixelStorei(GL_UNPACK_ALIGNMENT, 1);
    glPixelStorei(GL_PACK_ALIGNMENT, 1);

    glDisable(GL_DITHER);
    glDisable(GL_BLEND);
    glDisable(GL_CULL_FACE);
    glEnable(GL_NORMALIZE);

    glEnable(GL_LINE_SMOOTH);
    glHint(GL_LINE_SMOOTH_HINT, GL_NICEST);
    glShadeModel(GL_SMOOTH);

    glEnable(GL_DEPTH_TEST);
    glDepthMask(true);
    glDepthFunc(GL_LESS);

    // disable color materials, so that glMaterial calls work
    glDisable(GL_COLOR_MATERIAL);

    glEnable(GL_LIGHTING);
    glEnable(GL_LIGHT0);

    // lighting
    GLfloat ambientLight[] = {0.5f, 0.5f, 0.5f, 1.0f};
    GLfloat lightColor[] = {0.8f, 0.8f, 0.8f, 1.0f};

    glLightModelfv(GL_LIGHT_MODEL_AMBIENT, ambientLight);
    glLightfv(GL_LIGHT0, GL_DIFFUSE, lightColor);
    glLightfv(GL_LIGHT0, GL_SPECULAR, lightColor);

    glDisable(GL_LIGHTING);

    // ---------------------------------------------------------
    //setManipulatedFrame( new qglviewer::ManipulatedFrame() );

    {
#if 0
        QString vpath = this->parentApp->m_utilPath + "volshader.frag";
        vsvr = new VSVR(vpath.toStdString().c_str());
#endif
        vsvr = new VSVR();
    }
}

void TopoMSViewer::keyPressEvent(QKeyEvent* event){

    switch(event->key()){
        case 'R':
            this->camera()->centerScene();
            this->camera()->showEntireScene();
    }
    updateGL();
    QGLViewer::keyPressEvent(event);
}

